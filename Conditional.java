public class Conditional {
    public static void main(String args[])
    {
        int marks=98;
        if(marks>90)
        {
            System.out.println("A+");
        }
        else if(marks<90 && marks>85)
        {
            System.out.println("A");
        }
        else
        {
            System.out.println("pass");
        }
    }
}
