public class Operators {
    public static void main(String args[])
    {
        int numberOne=100;
        int numberTwo=5;
        System.out.println(numberOne+numberTwo);
        System.out.println(numberOne-numberTwo);
        System.out.println(numberOne*numberTwo);
        System.out.println(numberOne/numberTwo);
        System.out.println(numberOne%numberTwo);
    }
}
